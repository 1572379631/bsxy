package cn.gzsxt.pojo;

public class BulletinInfo {

	private int bulletinId;
	
	private String bulletinPerson;
	
	private String bulletinCon;
	
	private String bulletinTime;

	public int getBulletinId() {
		return bulletinId;
	}

	public void setBulletinId(int bulletinId) {
		this.bulletinId = bulletinId;
	}


	public String getBulletinPerson() {
		return bulletinPerson;
	}

	public void setBulletinPerson(String bulletinPerson) {
		this.bulletinPerson = bulletinPerson;
	}

	public String getBulletinCon() {
		return bulletinCon;
	}

	public void setBulletinCon(String bulletinCon) {
		this.bulletinCon = bulletinCon;
	}

	public String getBulletinTime() {
		return bulletinTime;
	}

	public void setBulletinTime(String bulletinTime) {
		this.bulletinTime = bulletinTime;
	}
	
	
}
