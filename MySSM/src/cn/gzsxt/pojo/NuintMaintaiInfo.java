public class NuintMaintaiInfo {

	private String muId;
	private String muName;
	private String muAddress;
	private String muTell;
	
	public String getMuId() {
		return muId;
	}
	public void setMuId(String muId) {
		this.muId = muId;
	}
	public String getMuName() {
		return muName;
	}
	public void setMuName(String muName) {
		this.muName = muName;
	}
	public String getMuAddress() {
		return muAddress;
	}
	public void setMuAddress(String muAddress) {
		this.muAddress = muAddress;
	}
	public String getMuTell() {
		return muTell;
	}
	public void setMuTell(String muTell) {
		this.muTell = muTell;
	}
	
	
}
